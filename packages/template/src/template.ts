export function renderWorkerClaudeMd(opts: { workerName: string }): string {
  const { workerName } = opts

  const sections = [
    `# Agent Instructions`,
    ``,
    `You are the **${workerName}** worker.`,
    ``,
    `## Workspace Layout`,
    ``,
    `Key paths relative to your worker root:`,
    ``,
    `- \`repo/\` ŌĆö the source code repository (your main workspace)`,
    `- \`assignment.md\` ŌĆö your current task (read this first)`,
    `- \`status.md\` ŌĆö write your current status here`,
    `- \`inbox/\` ŌĆö parcels and status delivered to you by other workers`,
    `  - \`inbox/<sender>-<hash>/\` ŌĆö a delivered parcel (\`meta.yaml\` plus an optional \`README.md\` note and/or \`squashed.diff\`)`,
    `  - \`inbox/status/<worker-name>.md\` ŌĆö latest status from another worker`,
    `  - \`inbox/.done/\` ŌĆö parcels you have already processed`,
    `- \`outbox/\` ŌĆö parcels you want delivered to other workers, addressed by recipient`,
    `  - \`outbox/<recipient>/\` ŌĆö a parcel for that worker; put a \`README.md\` note and any files in it`,
    ``,
    `## How to Work`,
    ``,
    `1. **Read your assignment**: Check \`assignment.md\` for your task`,
    `2. **Work in repo/**: Make changes and commit to the repo as you go`,
    `3. **Update your status**: Write progress to \`status.md\` periodically`,
    `4. **Commit against the baseline**: All your commits are measured against the \`quimby/seed\` tag`,
    ``,
    `## Status Updates`,
    ``,
    `Keep \`status.md\` current. Write a brief summary of:`,
    `- What you're working on`,
    `- Any blockers or questions`,
    `- When you're done, write "done" with a summary`,
    ``,
    `## Incoming Parcels`,
    ``,
    `Check \`inbox/\` for parcels and status from other workers.`,
    `- A parcel in \`inbox/<sender>-<hash>/\` holds a \`meta.yaml\` manifest plus an optional \`README.md\` note and/or \`squashed.diff\` of code`,
    `- When you have processed a parcel, move it to \`inbox/.done/\``,
    `- Status updates in \`inbox/status/<worker>.md\` show other workers' progress`,
    ``,
    `## Handing Work to Other Workers`,
    ``,
    `To send a parcel to another worker, create \`outbox/<recipient>/\` and write a \`README.md\` note (and any files) into it.`,
    `Your committed work is attached automatically; to attach a different worker's diff, add YAML frontmatter to the note:`,
    ``,
    `    ---`,
    `    attach: builder`,
    `    ---`,
    `    Promote builder's work; one nit in auth.ts.`,
    ``,
    `The user runs \`quimby handoff ${workerName}\` to deliver every queued parcel to its recipient.`,
    ``,
    `## Project Instructions`,
    ``,
    `@repo/CLAUDE.md`,
  ]

  return sections.join('\n') + '\n'
}
